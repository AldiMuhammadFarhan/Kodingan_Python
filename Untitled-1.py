# def adalah syntax sebuah fungsi
# perkenalan adalah nama fungsi
# (nama,fakultas) adalah paramater yang harus di isi, bisa 1 atau lebih dari 1
def perkenalan(nama, fakultas):
    # print adalah syntax untuk mencetak nilai yang ingin dicetak
    print('Pekenalkan saya '+ nama +' dari fakultas '+ fakultas)
# memanggil fungsi perkenalan dengan mengisi nama dan fakultas
perkenalan('Aldi Muhammad Farhan', 'informatika')
